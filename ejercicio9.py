calificacion_1 = float(input("Ingrese la calificación 1: "))
calificacion_2 = float(input("Ingrese la calificación 2: "))
calificacion_3 = float(input("Ingrese la calificación 3: "))

# Encontrar la calificación mayor
calificacion_mayor = max(calificacion_1, calificacion_2, calificacion_3)

# Encontrar la calificación menor
calificacion_menor = min(calificacion_1, calificacion_2, calificacion_3)

# Mostrar las calificaciones mayor y menor
print("La calificación mayor es:", calificacion_mayor)
print("La calificación menor es:", calificacion_menor)
